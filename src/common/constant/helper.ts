export const commonArrayElements = (firstArray: any, secondArray: any) => {
    if (firstArray.length < secondArray.length) {
        let tempArray = firstArray
        firstArray = secondArray
        secondArray = tempArray
    }
    return firstArray.filter((element: any) => secondArray.includes(element))
}
export const uncommonArrayElements = (firstArray, secondArray) => {
    if (firstArray.length < secondArray.length) {
        let tempArray = firstArray
        firstArray = secondArray
        secondArray = tempArray
    }

    return firstArray.filter((element) => !secondArray.includes(element))
}
